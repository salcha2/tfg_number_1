                    <p> Art-Risk es una herramienta informatizada para la conservación preventiva del patrimonio en los centros urbanos basada en modelos de inteligencia artificial.
                    Para su desarrollo se ha desarrollado un nuevo método de análisis de la vulnerabilidad y el riesgo de edificios singulares. Todo ello realizado por un equipo multidisciplinar.
                    Gracias a ello se puede evaluar la toma de decisiones en políticas de ordenación del territorio, urbanismo y tratamiento del Patrimonio Histórico, con el que por primera vez una se puede aplicar una visión transversal que incluye el valor patrimonial, urbanístico, arquitectónico, cultural, el análisis del entorno medioambiental y la situación socio-demográfica de la obra. 
                    Todo esto permite llevar a cabo una toma de decisiones basada en criterios científicos y minimizar así los riesgos de pérdidas de elementos patrimoniales.
						  Esta herramienta permite reproducir de una manera aproximada el razonamiento humano y las relaciones existentes entre los factores de vulnerabilidad, los factores de riesgo y los parámetros históricos del monumento mediante la teoría de los conjuntos difusos.
